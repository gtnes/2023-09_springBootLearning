package com.gtnes.model;

public class UserObject {
    private String name;
    private int age;

    public UserObject() {
        // 默认构造函数
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
